const test = require('node:test');
const assert = require('node:assert/strict');
const {
  cleanCopiedArticleText,
  fallbackTitleFromContent,
  pickBestArticleTitle,
} = require('../lib/content-text.js');

test('cleanCopiedArticleText strips markdown formatting and AI-flavored wrappers', () => {
  const input = [
    '# ?????????',
    '',
    '???????????',
    '',
    '**?????** ??????????',
    '',
    '- ????????',
    '- ??????????',
    '',
    '| ?? | ???? |',
    '| --- | --- |',
    '| ???? | 0-3? |',
    '',
    '```ts',
    "console.log('debug');",
    '```',
  ].join('\n');

  const output = cleanCopiedArticleText(input);

  assert.equal(output.includes('#'), false);
  assert.equal(output.includes('**'), false);
  assert.equal(output.includes('??????????'), false);
  assert.equal(output.includes('```'), false);
  assert.equal(output.includes('---'), false);
  assert.match(output, /?????????/);
  assert.match(output, /????????/);
  assert.match(output, /????\s+0-3?/);
});

test('fallbackTitleFromContent builds a real title instead of truncating the first 50 chars', () => {
  const content = [
    '?????????????????????????????',
    '???????????????????????????????????????',
    '???????????????????????',
  ].join('');

  const title = fallbackTitleFromContent(content);

  assert.equal(title.includes('????????'), false);
  assert.equal(title.length <= 24, true);
  assert.match(title, /???|??|??|??/);
});

test('pickBestArticleTitle prefers a clean AI title and falls back when the AI output is junk', () => {
  const content = '???????????????????????????';

  assert.equal(
    pickBestArticleTitle(content, '????????????3??'),
    '????????????3??'
  );

  const fallback = pickBestArticleTitle(content, '???????????????????????');
  assert.equal(fallback.includes('???????????'), false);
  assert.equal(fallback.length <= 24, true);
});